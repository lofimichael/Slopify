function loadAndDisplayNews() {
  try {
    const randomArticles = getRandomArticles(onionArticles.articles, 6);
    const leftArticles = randomArticles.slice(0, 3);
    const rightArticles = randomArticles.slice(3, 6);

    displayArticles(leftArticles, "news-column-left");
    displayArticles(rightArticles, "news-column-right");
  } catch (error) {
    console.error("Error:", error);
  }
}

function getRandomArticles(articles, count) {
  const shuffled = [...articles].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

function displayArticles(articles, columnClass) {
  const column = document.querySelector(`.${columnClass}`);
  column.innerHTML = articles
    .map(
      (article) => `
    <div style="
        background: rgba(40, 40, 40, 0.8);
        border-radius: 12px;
        overflow: hidden;
        box-shadow: 0 4px 6px rgba(0, 0, 0, 0.1);
        transition: transform 0.3s ease;
        cursor: pointer;
        margin-bottom: 20px;
    "
    onmouseover="this.style.transform='translateY(-5px)'"
    onmouseout="this.style.transform='translateY(0)'">
        <img 
            src="${article.image_url}" 
            alt="${article.title}"
            style="
                width: 100%;
                height: 200px;
                object-fit: cover;
            "
            onerror="this.src='https://via.placeholder.com/300x200?text=Image+Not+Available'"
        >
        <div style="padding: 20px;">
            <h3 style="
                font-size: 1.1rem;
                color: #ebdbb2;
                margin: 0;
                line-height: 1.4;
                font-family: Arial, sans-serif;
            ">${article.title}</h3>
            <div style="
                font-size: 0.8rem;
                color: #b8bb26;
                margin-top: 10px;
            ">${new Date().toLocaleDateString()}</div>
        </div>
    </div>
`,
    )
    .join("");
}

function handleResponsiveLayout() {
  const container = document.getElementById("news-container");

  if (window.innerWidth < 1600) {
    container.style.display = "none";
  } else {
    container.style.display = "block";
  }
}

document.addEventListener("DOMContentLoaded", () => {
  loadAndDisplayNews();
  handleResponsiveLayout();
});

window.addEventListener("resize", handleResponsiveLayout);
